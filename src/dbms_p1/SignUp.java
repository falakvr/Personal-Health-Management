    private void signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupActionPerformed
        // TODO add your handling code here:

        String firstname = enterFirstName.getText();
        String lastname = enterLastName.getText();
        String address = enterAddress.getText();
        String age = enterAge.getText();
        String email = enterEmail.getText();
        String contact = enterContact.getText();
        String dob = enterdob.getText();
        String passwd = addPassword.getText();
        String user_id = enterUserId.getText();

        if (user_id.isEmpty() || passwd.isEmpty() || firstname.isEmpty() || lastname.isEmpty() || email.isEmpty() || age.isEmpty() || address.isEmpty() || contact.isEmpty() || dob.isEmpty()
                || Gender.getSelection() == null || IsPatient.getSelection() == null || IsHealthSupporter.getSelection() == null) {
            JOptionPane.showMessageDialog(null, "Please provide all information.");
            return;
        }

        male.setActionCommand("Male");
        female.setActionCommand("Female");
        String gender = Gender.getSelection().getActionCommand();

        patientYes.setActionCommand("Y");
        patientNo.setActionCommand("N");
        String isPatient = IsPatient.getSelection().getActionCommand();

        hsYes.setActionCommand("Y");
        hsNo.setActionCommand("N");
        String isHS = IsHealthSupporter.getSelection().getActionCommand();

        try {
            String query_login = "INSERT INTO Login VALUES ('"
                    + user_id + "','" + passwd + "')";
            System.out.println(query_login);
            Setup.stmt.executeUpdate(query_login);

            String query_people = "INSERT INTO People VALUES ('"
                    + user_id + "','" + firstname + "','" + lastname + "','" + address
                    + "','" + gender + "'," + age + ",'" + dob + "','" + isPatient + "','" + isHS + "'," + contact
                    + ",'" + email + "')";
            System.out.println(query_people);
            Setup.stmt.executeUpdate(query_people);

            JOptionPane.showMessageDialog(null, "New Account created!");

//
//            if (isPatient.equals("Y")) {
//                String query_patient = "INSERT INTO Patient VALUES ('"
//                        + user_id + "','N')";
//                String query_well = "INSERT INTO Patient VALUES ('"
//                        + user_id + "', null, null)";
//                System.err.println("Patient table insert query is - \n" + query_patient);
//                System.err.println("Well Patient table insert query is - \n" + query_well);
//                Setup.stmt.executeUpdate(query_patient);
//                Setup.stmt.executeUpdate(query_well);
//            }


            this.setVisible(false);
            new Login().setVisible(true);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_signupActionPerformed

