    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        try {
            // TODO add your handling code here:
            String obstype = addObstype.getText();
            String obsvalue = addObsValue.getText();
            String date = addObsTime.getText();
            System.out.println(date);
            String currentTimestamp = getCurrentTimeStamp();

            if (obstype.isEmpty() || obsvalue.isEmpty() || date.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter all fields");
            } else {

                Setup.stmt.executeUpdate("INSERT INTO Observation "
                        + "VALUES ('" + Setup.loginuserid + "', '"
                        + obstype + "', TO_DATE('" + currentTimestamp + "', 'YYYY-MM-DD HH24:MI:SS'), '"
                        + obsvalue + "', TO_DATE('" + date + "', 'YYYY-MM-DD HH24:MI:SS'))");

                if (Setup.logintype.equals("Patient")) {
                    Patient patient = new Patient();
                    patient.setVisible(true);
                } else if (Setup.logintype.equals("Health_Supporter")) {
                    HSPatientInfo patinfo = new HSPatientInfo();
                    patinfo.setVisible(true);
                }
                this.setVisible(false);
            }
        } catch (SQLException ex) {
            System.out.println("New Observation entry error");
            ex.printStackTrace();
            Logger.getLogger(AddObservation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addActionPerformed

